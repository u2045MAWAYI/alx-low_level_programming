#include <stdio.h>

/**
 *  add - Adds two integers.
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: The sum of a and b.
 */
int add(int a, int b)
{
    return a + b;
}
/**
 *  sub - subtact two integers.
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: subtracted
 */
int sub(int a, int b)
{
    return a - b;
}
/**
 *  div - divide two integers.
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: divided
 */
int div(int a, int b)
{
    return a / b;
}
/**
 *  mul - multiply two integers.
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: multiply
 */
int mul(int a, int b)
{
    return a * b;
}
/**
 *  mod - mod two integers.
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: final answer
 */
int mod(int a, int b)
{
    return a % b;
}
